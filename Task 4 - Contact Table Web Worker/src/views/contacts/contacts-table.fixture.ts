// Function to generate a random cell update event
export const getRandomCellUpdate = (): { rowId: string; colId: string; value: string } => {
  // Get random row ID
  const randomRowIndex = Math.floor(Math.random() * mockRows.length)
  const rowId = mockRows[randomRowIndex].id

  // Get random column ID
  const randomColIndex = Math.floor(Math.random() * mockColumns.length)
  const colId = mockColumns[randomColIndex].id

  // Get current cell value
  const row = mockRows[randomRowIndex]
  const cell = row.cells.find(c => c.colId === colId)
  const currentValue = cell?.value || ''

  // Generate new value based on current value + timestamp
  const timestamp = Date.now()
  const value = `${currentValue} [${timestamp}]`

  return { rowId, colId, value }
}
